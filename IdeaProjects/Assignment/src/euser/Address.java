package euser;

public class Address {
     String houseNumber;
     String street;
     String city;
     String state;
     String countryName;

    public Address(String houseNumber, String street, String city, String state, String countryName) {
        this.houseNumber = houseNumber;
        this.street = street;
        this.city = city;
        this.state = state;
        this.countryName = countryName;
    }
}
